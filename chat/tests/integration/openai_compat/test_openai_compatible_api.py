@pytest.mark.integration
def test_chat_completions_scrapalot_extras_passthrough(
    openai_session: requests.Session,
    test_workspace: dict,
    test_collection: dict,
):
    """`scrapalot.collection_ids` overrides slug-resolved collection — proves
    the extras block reaches OpenAICompatibleService.buildChatRequest and
    passes into ChatService.generateChat without losing the routing target.
    """
    workspace_slug = test_workspace.get("slug") or "default"
    response = openai_session.post(
        f"{GATEWAY_URL}/chat/completions",
        json={
            "model": f"scrapalot:{workspace_slug}",
            "messages": [{"role": "user", "content": "Reply with exactly the word: PONG"}],
            "stream": False,
            "scrapalot": {
                # Force RAG against this specific collection even though the
                # model slug points at the workspace root.
                "collection_ids": [str(test_collection["id"])],
                "top_k": 5,
                "similarity_threshold": 0.4,
            },
        },
        timeout=120,
    )
    assert response.status_code == 200, response.text
    body = response.json()
    assert body["object"] == "chat.completion"
    assert body["choices"][0]["message"]["content"].strip(), "Empty assistant content with extras passthrough"


@pytest.mark.integration
def test_chat_completions_stream_passthrough_scrapalot_packets(
    openai_session: requests.Session,
    test_workspace: dict,
    test_collection: dict,
):
    """Stream against a collection (RAG) must emit at least one
    `delta.scrapalot` chunk (citation_info / status / etc.) on top of the
    token stream — proves PR1 packet passthrough works end-to-end.
    """
    workspace_slug = test_workspace.get("slug") or "default"
    response = openai_session.post(
        f"{GATEWAY_URL}/chat/completions",
        json={
            "model": f"scrapalot:{workspace_slug}",
            "messages": [{"role": "user", "content": "What is the main theme of the document?"}],
            "stream": True,
            "scrapalot": {"collection_ids": [str(test_collection["id"])]},
        },
        stream=True,
        timeout=180,
        headers={"Accept": "text/event-stream"},
    )
    assert response.status_code == 200, response.text

    saw_scrapalot_packet = False
    saw_done = False
    for raw in response.iter_lines(decode_unicode=True):
        if not raw or not raw.startswith("data:"):
            continue
        payload = raw[len("data:") :].strip()
        if payload == "[DONE]":
            saw_done = True
            break
        chunk = json.loads(payload)
        delta = chunk["choices"][0].get("delta") or {}
        if isinstance(delta.get("scrapalot"), dict):
            saw_scrapalot_packet = True

    assert saw_done, "Stream did not terminate with [DONE]"
    assert saw_scrapalot_packet, "Expected at least one delta.scrapalot chunk in RAG stream (citation_info/status/etc.)"


