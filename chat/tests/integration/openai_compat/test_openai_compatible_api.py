"""
Integration tests for the OpenAI-compatible API surface.

Endpoints (all on the Kotlin backend, reached through the gateway):
    GET  /api/v1/models
    GET  /api/v1/models/{id}
    POST /api/v1/chat/completions          # SSE when stream=True, JSON otherwise

Auth: Authorization: Bearer scp-... (an API key generated by
POST /api/v1/auth/api-keys). The legacy X-API-Key header still works for
existing CLI users; this test exercises the Bearer path because that is what
the openai-python and openai-js SDKs send.

Coverage:
  * GET  /v1/models       lists workspaces and collections as model ids
  * POST /v1/chat/completions stream=False returns a chat.completion JSON
  * POST /v1/chat/completions stream=True  returns SSE with [DONE] terminator
  * Auth: missing Bearer → 401
  * Slug routing: unknown workspace slug → 404
"""

