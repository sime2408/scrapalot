@pytest.mark.integration
def test_chat_completions_stream(openai_session: requests.Session, test_workspace: dict):
    """POST stream=True → SSE chunks ending with `data: [DONE]`."""
    workspace_slug = test_workspace.get("slug") or "default"
    response = openai_session.post(
        f"{GATEWAY_URL}/chat/completions",
        json={
            "model": f"scrapalot:{workspace_slug}",
            "messages": [{"role": "user", "content": "Reply with exactly the word: PONG"}],
            "stream": True,
        },
        stream=True,
        timeout=120,
        headers={"Accept": "text/event-stream"},
    )
    assert response.status_code == 200, response.text
    assert response.headers["Content-Type"].startswith("text/event-stream"), response.headers

    chunks: list[dict] = []
    saw_done = False
    saw_finish_stop = False

    for raw in response.iter_lines(decode_unicode=True):
        if not raw or not raw.startswith("data:"):
            continue
        payload = raw[len("data:") :].strip()
        if payload == "[DONE]":
            saw_done = True
            break
        chunk = json.loads(payload)
        assert chunk["object"] == "chat.completion.chunk"
        chunks.append(chunk)
        if (chunk["choices"][0].get("finish_reason")) == "stop":
            saw_finish_stop = True

    assert chunks, "Expected at least one chat.completion.chunk before [DONE]"
    assert saw_done, "Stream did not terminate with `data: [DONE]`"
    assert saw_finish_stop, "No chunk carried finish_reason=stop"

    # Reconstruct the assistant text from delta.content fragments.
    text = "".join(c["choices"][0]["delta"].get("content") or "" for c in chunks)
    assert text.strip(), f"Empty assistant text reconstructed from chunks: {chunks!r}"


