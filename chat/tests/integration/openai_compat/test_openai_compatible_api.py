@pytest.mark.integration
def test_chat_completions_non_stream(openai_session: requests.Session, test_workspace: dict):
    """POST stream=False → single chat.completion JSON with assistant content."""
    workspace_slug = test_workspace.get("slug") or "default"
    response = openai_session.post(
        f"{GATEWAY_URL}/chat/completions",
        json={
            "model": f"scrapalot:{workspace_slug}",
            "messages": [{"role": "user", "content": "Reply with exactly the word: PONG"}],
            "stream": False,
        },
        timeout=120,
    )
    assert response.status_code == 200, response.text

    body = response.json()
    assert body["object"] == "chat.completion"
    assert body["model"] == f"scrapalot:{workspace_slug}"
    assert body["id"].startswith("chatcmpl-"), body["id"]

    choices = body["choices"]
    assert len(choices) == 1
    msg = choices[0]["message"]
    assert msg["role"] == "assistant"
    assert isinstance(msg["content"], str) and msg["content"].strip(), "Assistant content must not be empty"


