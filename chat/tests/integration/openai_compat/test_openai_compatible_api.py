@pytest.fixture
def api_key(authenticated_session: requests.Session) -> str:
    """Mint a Scrapalot API key via the existing /auth/api-keys endpoint."""
    name = f"openai-compat-test-{uuid.uuid4().hex[:8]}"
    response = authenticated_session.post(
        f"{GATEWAY_URL}/auth/api-keys",
        json={"name": name},
        timeout=30,
    )
    assert response.status_code == 201, f"Failed to create API key: {response.status_code} {response.text}"
    payload = response.json()
    plain = payload.get("plainTextKey") or payload.get("plain_text_key")
    assert plain and plain.startswith("scp-"), f"Expected scp-... plaintext key, got: {payload}"
    yield plain

    # Best-effort cleanup so the user's key list does not bloat across runs.
    key_id = payload.get("id")
    if key_id:
        authenticated_session.delete(f"{GATEWAY_URL}/auth/api-keys/{key_id}", timeout=15)


@pytest.fixture
def openai_session(api_key: str) -> requests.Session:
    """A bare requests session that authenticates exactly like the OpenAI SDK."""
    s = requests.Session()
    s.headers.update({"Authorization": f"Bearer {api_key}"})
    return s


