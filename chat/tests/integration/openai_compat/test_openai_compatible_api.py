@pytest.mark.integration
def test_v1_models_lists_workspaces(openai_session: requests.Session, test_workspace: dict):
    """GET /v1/models returns the OpenAI list envelope and includes the test workspace."""
    response = openai_session.get(f"{GATEWAY_URL}/models", timeout=15)
    assert response.status_code == 200, response.text

    body = response.json()
    assert body["object"] == "list"
    assert isinstance(body["data"], list) and body["data"], "Expected at least the test workspace in /v1/models"

    ids = {entry["id"] for entry in body["data"]}
    workspace_slug = test_workspace.get("slug")
    if workspace_slug:
        assert f"scrapalot:{workspace_slug}" in ids, f"Expected scrapalot:{workspace_slug} in /v1/models, got {sorted(ids)[:10]}"

    for entry in body["data"]:
        assert entry["object"] == "model"
        assert entry["owned_by"] == "scrapalot"
        assert isinstance(entry["created"], int)


